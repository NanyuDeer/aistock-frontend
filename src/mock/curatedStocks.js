export const curatedStockProfiles = tenbaggerProfiles.reduce((profiles, profile) => {
  const current = profiles[profile.code];
  if (!current) {
    profiles[profile.code] = profile;
    return profiles;
  }

  profiles[profile.code] = {
    ...current,
    ...profile,
    category: Array.from(new Set([...(current.category || []), ...(profile.category || [])]))
  };
  return profiles;
}, {});

export const curatedStockCodes = Object.keys(curatedStockProfiles);

export const getCuratedStockProfile = (code) => {
  if (!code) return null;
  return curatedStockProfiles[String(code)] || null;
};

export const isCuratedStock = (code) => Boolean(getCuratedStockProfile(code));
